package entidades;

public class Pessoa {
	
	String nome;
	String email;
	float renda;
	
	public Pessoa (String nome, String email, float renda) {
		this.nome = nome;
		this.email = email;
		this.renda = renda;
	}
	
	public String getNome() {

		return nome;
	}
	
	public String getEmail() {

		return email;
	}
	
	public float getRenda() {
		
		return renda;
	}
	
	public float Divida() {
		
		return renda;
	}

}
